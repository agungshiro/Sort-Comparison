/**
 * Sorting, Abstract for any sorting class.
 * @author agung wahyudiono
 *
 */

public abstract class Sorting {
	
	/**
	 * Abstract method for doing sort
	 */
	abstract int[] doSort();
	
	/**
	 * Abstract method to set an array to be sorted
	 * @param arr, integer array
	 */
	abstract void setArray(int[] arr);
	
}
